#include <iostream>
/*This program print the characters V to Z on the same line with each pair
of adjacent characters separated by one space*/ 
int main() {
	std::cout << "VX " << "YZ" << std::endl;
	std::cout << "V" << "X" << " " << "Y" << "Z" << std::endl;
	std::cout << "VX ";
	std::cout << "YZ";
}